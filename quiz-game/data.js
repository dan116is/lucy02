  function genSeq(t) { let n = 0, g = 0; while (n < t && g++ < t * 40) { const s0 = 1 + rnd(20), d = 2 + rnd(12), s = [s0, s0 + d, s0 + 2 * d, s0 + 3 * d]; if (numQ(`מהו האיבר הבא בסדרה: ${s.join(', ')}, ?`, s0 + 4 * d, 'logic', d + 2, `הפרש קבוע של ${d}`)) n++; } }
  function genSeqGeo(t) { let n = 0, g = 0; while (n < t && g++ < t * 40) { const s0 = 1 + rnd(6), q = 2 + rnd(2), s = [s0, s0 * q, s0 * q * q, s0 * q * q * q]; if (numQ(`מהו האיבר הבא בסדרה: ${s.join(', ')}, ?`, s0 * Math.pow(q, 4), 'logic', 20, `כל איבר כפול ${q}`)) n++; } }
