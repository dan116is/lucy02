  function toRoman(n) {
    const m = [[1000, 'M'], [900, 'CM'], [500, 'D'], [400, 'CD'], [100, 'C'], [90, 'XC'],
      [50, 'L'], [40, 'XL'], [10, 'X'], [9, 'IX'], [5, 'V'], [4, 'IV'], [1, 'I']];
    let r = ''; for (const [v, s] of m) while (n >= v) { r += s; n -= v; } return r;
  }
  function genRoman() {
    const vals = [];
    for (let i = 1; i <= 50; i++) vals.push(i);
    [60, 70, 80, 90, 100, 200, 400, 500, 900, 1000].forEach(v => vals.push(v));
    const allRoman = vals.map(toRoman);
    for (const n of vals) {
      const r = toRoman(n);
      add(`כיצד כותבים את המספר ${n} בספרות רומיות?`, distract(r, allRoman, 3), 0, 'history');
      add(`מהו ערכה של הספרה הרומית ${r}?`, distract(String(n), vals.map(String), 3), 0, 'history');
    }
  }

