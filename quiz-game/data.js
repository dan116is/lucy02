  function add(q, opts, correct, cat, x) {
    const key = cat + '|' + q;
    if (seen.has(key)) return false;
    seen.add(key);
    // ערבוב התשובות תוך שמירה על המיקום הנכון
    const correctVal = opts[correct];
    const sh = shuffle(opts.slice());
    bank.push({ q, a: sh, c: sh.indexOf(correctVal), cat, x: x || '' });
    return true;
  }

  // שאלה מספרית: בונה 3 מסיחים סבירים סביב התשובה
  function numQ(q, answer, cat, spread, x) {
    const opts = new Set([answer]);
    let guard = 0;
    while (opts.size < 4 && guard++ < 60) {
      let d = (rnd(2) ? 1 : -1) * (1 + rnd(spread));
      const v = answer + d;
      if (v >= 0 && !opts.has(v)) opts.add(v);
    }
    // השלמה אם נתקענו
    let k = answer + 1;
    while (opts.size < 4) { if (!opts.has(k)) opts.add(k); k++; }
    return add(q, [...opts], 0, cat, x);
  }

